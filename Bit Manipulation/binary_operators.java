/**
 * binary_operators
 */
import java.util.*;
public class binary_operators {
    public static void main(String args[]){
        //Binary AND
        System.out.println("AND operator 5 & 6: " + (5&6));
        //Binary OR
        System.out.println("OR operator 5 | 6: " + (5|6));
        //Binary XOR
        System.out.println("XOR operator 5 ^ 6: " + (5^6));
        //Binary One's Compliment
        System.out.println("AND operator 5: " + (~5));
        System.out.println("AND operator 5: " + (~0));
        //Binary Left Shift
        System.out.println("AND operator 5 << 6: " + (5<<2));
        //Binary Right shift
        System.out.println("AND operator 5 >> 6: " + (6>>1));
    }
}